
public class ModuloBeispiel {

	public static void main(String[] args) {

		int quotient, rest;

	    quotient  =  17 / 3;
	    rest =  17 % 3;
	    System.out.println("Der Quotient : " + quotient );
	    System.out.println("Der Rest: " + rest );
	    System.out.println("Das Original : " +
	        (quotient*3 + rest) );
	    
	}

}
